# Comparison Operator

x = 5
y = 4
print(x == y) # True if both the values are equal

print(3 == 3)

print(x != y) # True if both the values are not equal

              # Less than (<) and Greater than (>)

print(x >y)
print(x<y)

print(x >= y) # Greater than or equal to >=

print(10 <= 12) # Less than or equal to <=

# Assignment Operator

